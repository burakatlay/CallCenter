    private void subProjectAddTable(Project project) throws ClassNotFoundException, SQLException {
        SubProjectProcesses subProjectProcesses = new SubProjectProcesses();
        int i = TableSubProject.getRowCount();

        subProjectModel = (DefaultTableModel) TableSubProject.getModel();
        for (int j = 0; j < i; j++) {
            subProjectModel.removeRow(0);
        }
        subProjectData = new Object[4];
        for (SubProject subProject : subProjectProcesses.getSubProjectList(project)) {
            subProjectData[0] = subProject.getSubProjectId();
            subProjectData[1] = subProject.getSubProjectName();
            subProjectData[2] = subProject.getPrice();
            subProjectData[3] = subProject.getCallType();
            subProjectModel.addRow(subProjectData);
        }
    }

    private void subProjectInsert(SubProject subProject) throws ClassNotFoundException, SQLException {
        SubProjectProcesses subProjectProcesses = new SubProjectProcesses();
        subProjectProcesses.subProjectInsert(subProject);
        this.subProjectAddTable(project);
    }
    
