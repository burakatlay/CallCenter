    private void projectAddTable() throws ClassNotFoundException, SQLException {
        ProjectProcesses pjProcesses = new ProjectProcesses();
        int i = TableProject.getRowCount();

        projectModel = (DefaultTableModel) TableProject.getModel();
        for (int j = 0; j < i; j++) {
            projectModel.removeRow(0);
        }
        projectData = new Object[2];
        for (Project projectList : pjProcesses.getProjectList()) {
            projectData[0] = projectList.getProjectId();
            projectData[1] = projectList.getProjectName();
            projectModel.addRow(projectData);
            Project project = new Project(projectList.getProjectId(), projectList.getProjectName());
            this.subProjectAddTable(project);
        }

    }

